class Cat:
    def __init__(self,first_name,pet_class,gender,cat_class,age,reg_doc,status):
        self.first_name=first_name
        self.pet_class=pet_class
        self.gender=gender
        self.cat_class=cat_class
        self.age=age
        self.reg_doc=reg_doc
        self.status=status
    def get_first_name(self):
        return self.first_name
    def get_pet_class(self):
        return self.pet_class
    def gender(self):
        return self.gender
    def cat_class(self):
        return self.cat_class
    def age(self):
        return self.age
    def reg_doc(self):
        return self.reg_doc
    def status(self):
        return self.status
